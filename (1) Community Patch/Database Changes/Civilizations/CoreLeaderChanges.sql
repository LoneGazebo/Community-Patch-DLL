WHERE LeaderType = 'LEADER_GANDHI'
AND FlavorType IN ('FLAVOR_NUKE', 'FLAVOR_USE_NUKE')
AND EXISTS (SELECT 1 FROM Defines WHERE Name = 'DIPLOAI_ENABLE_NUCLEAR_GANDHI' AND Value < 1);

