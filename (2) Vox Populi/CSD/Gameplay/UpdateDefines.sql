-- AI Diplomacy Desire and Distaste Modifiers.
-- Higher Desire = Higher diplomacy desire.
-- Lower distaste = More sensitive to other player's diplomatic actions and/or city-state abuse.

-- LESS COMPETITIVE
UPDATE Defines SET Value = '1' WHERE Name = 'NEED_DIPLOMAT_DESIRE_MODIFIER' AND EXISTS (SELECT * FROM CSD WHERE Type='CSD_DIFFICULTY' AND Value= 1 );
UPDATE Defines SET Value = '7' WHERE Name = 'NEED_DIPLOMAT_DISTASTE_MODIFIER' AND EXISTS (SELECT * FROM CSD WHERE Type='CSD_DIFFICULTY' AND Value= 1 );

-- MORE COMPETITIVE
UPDATE Defines SET Value = '3' WHERE Name = 'NEED_DIPLOMAT_DESIRE_MODIFIER' AND EXISTS (SELECT * FROM CSD WHERE Type='CSD_DIFFICULTY' AND Value= 3 );
UPDATE Defines SET Value = '5' WHERE Name = 'NEED_DIPLOMAT_DISTASTE_MODIFIER' AND EXISTS (SELECT * FROM CSD WHERE Type='CSD_DIFFICULTY' AND Value= 3 );

-- EXTREMELY COMPETITIVE
UPDATE Defines SET Value = '4' WHERE Name = 'NEED_DIPLOMAT_DESIRE_MODIFIER' AND EXISTS (SELECT * FROM CSD WHERE Type='CSD_DIFFICULTY' AND Value= 4 );
UPDATE Defines SET Value = '4' WHERE Name = 'NEED_DIPLOMAT_DISTASTE_MODIFIER' AND EXISTS (SELECT * FROM CSD WHERE Type='CSD_DIFFICULTY' AND Value= 4 );

