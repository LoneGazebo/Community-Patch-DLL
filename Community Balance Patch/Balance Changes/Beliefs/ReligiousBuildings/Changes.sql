UPDATE Building_YieldChanges
SET Yield = '0'
WHERE BuildingType = 'BUILDING_MOSQUE' AND YieldType = 'YIELD_CULTURE' AND EXISTS (SELECT * FROM COMMUNITY WHERE Type='COMMUNITY_CORE_BALANCE_RELIGION' AND Value= 1 );

